
public class MultiplyWithoutAstericks{
public static int MultiplicationWithoutAstericksFunction(int number1, int number2){


	int total = 0;


	if(number1 <= 0 && number2 <= 0){
		System.out.print("invalid number");

}

	for(int count = 0; count < number2; count++) {

	total = total + number1;



}

	return total;


}
}